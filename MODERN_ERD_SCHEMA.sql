-- 1. TABLES
CREATE TABLE TAI_KHOAN ( MA_TK NUMBER PRIMARY KEY, TEN_DN VARCHAR2(50) UNIQUE, MAT_KHAU VARCHAR2(100), VAI_TRO VARCHAR2(20) );
CREATE TABLE LOAI_PHONG ( MA_LOAI NUMBER PRIMARY KEY, TEN VARCHAR2(50), GIA NUMBER, GIA_DAT_COC NUMBER, DIEN_TICH NUMBER );
CREATE TABLE PHONG ( MA_PHONG NUMBER PRIMARY KEY, TEN_PHONG VARCHAR2(50), TRANG_THAI VARCHAR2(20), SO_NGUOI_TOI_DA NUMBER, TANG NUMBER, MA_LOAI NUMBER, DIEN_HT NUMBER DEFAULT 0, NUOC_HT NUMBER DEFAULT 0, G_DIEN NUMBER DEFAULT 4000, G_NUOC NUMBER DEFAULT 25000, FOREIGN KEY (MA_LOAI) REFERENCES LOAI_PHONG(MA_LOAI) );
CREATE TABLE TAI_SAN ( MA_TS NUMBER PRIMARY KEY, TEN_TS VARCHAR2(100), TINH_TRANG VARCHAR2(100), GIA_TRI NUMBER DEFAULT 0, TI_LE_MOI NUMBER DEFAULT 100, MA_PHONG NUMBER, FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG) );
CREATE TABLE NGUOI_THUE ( MA_KH NUMBER PRIMARY KEY, HO_TEN VARCHAR2(100), SDT VARCHAR2(20), CCCD VARCHAR2(20), MA_TK NUMBER, FOREIGN KEY (MA_TK) REFERENCES TAI_KHOAN(MA_TK) );
CREATE TABLE HOP_DONG ( MA_HD NUMBER PRIMARY KEY, MA_PHONG NUMBER, MA_KH NUMBER, NGAY_KY DATE, NGAY_HET_HAN DATE, TIEN_COC NUMBER, GIA_PHONG NUMBER, TRANG_THAI VARCHAR2(20) DEFAULT 'CON_HIEU_LUC', FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG), FOREIGN KEY (MA_KH) REFERENCES NGUOI_THUE(MA_KH) );
CREATE TABLE LOAI_DICH_VU ( MA_LDV NUMBER PRIMARY KEY, TEN_LOAI VARCHAR2(50) );
CREATE TABLE DICH_VU ( MA_DV NUMBER PRIMARY KEY, TEN_DV VARCHAR2(50), DON_GIA NUMBER, DON_VI VARCHAR2(20), MA_LDV NUMBER, FOREIGN KEY (MA_LDV) REFERENCES LOAI_DICH_VU(MA_LDV) );
CREATE TABLE HOA_DON ( MA_HD_BILL NUMBER PRIMARY KEY, MA_HD NUMBER, THANG_NAM VARCHAR2(7), D_CU NUMBER, D_MOI NUMBER, N_CU NUMBER, N_MOI NUMBER, T_PHONG NUMBER, T_DIEN NUMBER, T_NUOC NUMBER, TONG_TIEN NUMBER, NGAY_LAP DATE DEFAULT SYSDATE, TRANG_THAI VARCHAR2(20) DEFAULT 'CHUA_TRA', GHI_CHU VARCHAR2(200), FOREIGN KEY (MA_HD) REFERENCES HOP_DONG(MA_HD) );
CREATE TABLE THANH_VIEN ( MA_TV NUMBER PRIMARY KEY, MA_PHONG NUMBER, MA_KH NUMBER, FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG), FOREIGN KEY (MA_KH) REFERENCES NGUOI_THUE(MA_KH) );

