-- 2. LOGIC (FUNCTION & VIEWS)
CREATE OR REPLACE FUNCTION F_DEM_NGUOI(p_ma NUMBER) RETURN NUMBER IS v_count NUMBER; BEGIN SELECT COUNT(*) INTO v_count FROM ( SELECT MA_KH FROM HOP_DONG WHERE MA_PHONG = p_ma AND TRANG_THAI = 'CON_HIEU_LUC' UNION SELECT MA_KH FROM THANH_VIEN WHERE MA_PHONG = p_ma ); RETURN v_count; END;
/
CREATE OR REPLACE VIEW V_DS_PHONG AS SELECT P.MA_PHONG, P.TEN_PHONG, L.TEN AS LOAI, L.GIA, P.TRANG_THAI, P.SO_NGUOI_TOI_DA, F_DEM_NGUOI(P.MA_PHONG) AS SO_NGUOI, P.TANG, P.DIEN_HT, P.NUOC_HT FROM PHONG P JOIN LOAI_PHONG L ON P.MA_LOAI = L.MA_LOAI;
CREATE OR REPLACE VIEW V_HOPDONG AS SELECT H.MA_HD, H.MA_PHONG, N.HO_TEN, P.TEN_PHONG, H.NGAY_KY, H.NGAY_HET_HAN, H.TIEN_COC, H.TRANG_THAI, H.GIA_PHONG FROM HOP_DONG H JOIN NGUOI_THUE N ON H.MA_KH = N.MA_KH JOIN PHONG P ON H.MA_PHONG = P.MA_PHONG;

