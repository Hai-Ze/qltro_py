-- SOURCE FOR VIEW: V_HOADON
CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##QL_TRO"."V_HOADON" ("MA_HD", "MA_KH", "THANG_NAM", "TONG_TIEN", "NGAY_LAP", "TRANG_THAI") AS 
  SELECT "MA_HD","MA_KH","THANG_NAM","TONG_TIEN","NGAY_LAP","TRANG_THAI" FROM HOA_DON;

-- SOURCE FOR VIEW: V_HOADON_DV
CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##QL_TRO"."V_HOADON_DV" ("MA_HD", "TEN_DV") AS 
  SELECT H.MA_HD, D.TEN_DV
FROM HOA_DON_DICH_VU H
JOIN DICH_VU D ON H.MA_DV = D.MA_DV;

-- SOURCE FOR VIEW: V_TOP_HOADON
CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##QL_TRO"."V_TOP_HOADON" ("MA_HD", "MA_KH", "THANG_NAM", "TONG_TIEN", "NGAY_LAP", "TRANG_THAI") AS 
  SELECT "MA_HD","MA_KH","THANG_NAM","TONG_TIEN","NGAY_LAP","TRANG_THAI" FROM (
    SELECT * FROM HOA_DON ORDER BY TONG_TIEN DESC
)
WHERE ROWNUM <= 5;

