-- 3. VIEWS
CREATE OR REPLACE VIEW V_DS_PHONG AS 
SELECT P.MA_PHONG, P.TEN_PHONG, L.TEN AS LOAI, L.GIA, P.TRANG_THAI, P.SO_NGUOI_TOI_DA, 
(SELECT COUNT(*) FROM (SELECT MA_KH FROM HOP_DONG WHERE MA_PHONG = P.MA_PHONG AND TRANG_THAI = 'CON_HIEU_LUC' UNION SELECT MA_KH FROM THANH_VIEN WHERE MA_PHONG = P.MA_PHONG)) AS SO_NGUOI, 
P.TANG, P.DIEN_HT, P.NUOC_HT FROM PHONG P JOIN LOAI_PHONG L ON P.MA_LOAI = L.MA_LOAI;

CREATE OR REPLACE VIEW V_HOPDONG AS 
SELECT H.MA_HD, H.MA_PHONG, N.HO_TEN, P.TEN_PHONG, H.NGAY_KY, H.TIEN_COC, H.GIA_PHONG, H.TRANG_THAI 
FROM HOP_DONG H JOIN NGUOI_THUE N ON H.MA_KH = N.MA_KH JOIN PHONG P ON H.MA_PHONG = P.MA_PHONG;

-- 4. TRIGGERS
CREATE OR REPLACE TRIGGER TRG_HD_DAT_PHONG
AFTER INSERT ON HOP_DONG
FOR EACH ROW
BEGIN
    UPDATE PHONG SET TRANG_THAI = 'DA_THUE' WHERE MA_PHONG = :NEW.MA_PHONG;
END;
/

CREATE OR REPLACE TRIGGER TRG_HD_TRA_PHONG
AFTER UPDATE ON HOP_DONG
FOR EACH ROW
WHEN (NEW.TRANG_THAI = 'HET_HAN')
BEGIN
    UPDATE PHONG SET TRANG_THAI = 'TRONG' WHERE MA_PHONG = :NEW.MA_PHONG;
END;
/

CREATE OR REPLACE TRIGGER TRG_KIEM_TRA_TRONG
BEFORE INSERT ON HOP_DONG
FOR EACH ROW
DECLARE
    v_status VARCHAR2(20);
BEGIN
    SELECT TRANG_THAI INTO v_status FROM PHONG WHERE MA_PHONG = :NEW.MA_PHONG;
    IF v_status = 'DA_THUE' THEN
        RAISE_APPLICATION_ERROR(-20001, '⚠️ LỖI ERP: Phòng này đang có khách ở!');
    END IF;
END;
/


