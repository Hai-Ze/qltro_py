
-- 0. CLEANUP (XÓA SẠCH ĐỂ LÀM MỚI)
-- Dùng mã lệnh an toàn, nếu không có bảng thì bỏ qua chứ không báo lỗi
BEGIN
    FOR r IN (SELECT table_name FROM user_tables WHERE table_name IN ('CHI_PHI','THANH_VIEN','TAI_SAN','HOA_DON','HOP_DONG','NGUOI_THUE','PHONG','LOAI_PHONG','DICH_VU')) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || r.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;
    FOR s IN (SELECT sequence_name FROM user_sequences WHERE sequence_name IN ('SEQ_LOAI','SEQ_PHONG','SEQ_KH','SEQ_HD','SEQ_HOADON','SEQ_TS','SEQ_TV','SEQ_CHIPHI')) LOOP
        EXECUTE IMMEDIATE 'DROP SEQUENCE ' || s.sequence_name;
    END LOOP;
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

-- 1. TABLES
CREATE TABLE LOAI_PHONG ( MA_LOAI NUMBER PRIMARY KEY, TEN VARCHAR2(50), GIA NUMBER, GIA_DAT_COC NUMBER, DIEN_TICH NUMBER );
CREATE TABLE PHONG ( MA_PHONG NUMBER PRIMARY KEY, TEN_PHONG VARCHAR2(50), TRANG_THAI VARCHAR2(20) DEFAULT 'TRONG', SO_NGUOI_TOI_DA NUMBER, TANG NUMBER, MA_LOAI NUMBER, DIEN_HT NUMBER DEFAULT 0, NUOC_HT NUMBER DEFAULT 0, FOREIGN KEY (MA_LOAI) REFERENCES LOAI_PHONG(MA_LOAI) );
CREATE TABLE NGUOI_THUE ( MA_KH NUMBER PRIMARY KEY, HO_TEN VARCHAR2(100), SDT VARCHAR2(20), CCCD VARCHAR2(20) );
CREATE TABLE HOP_DONG ( MA_HD NUMBER PRIMARY KEY, MA_PHONG NUMBER, MA_KH NUMBER, NGAY_KY DATE DEFAULT SYSDATE, NGAY_HET_HAN DATE, TIEN_COC NUMBER, GIA_PHONG NUMBER, TRANG_THAI VARCHAR2(20) DEFAULT 'CON_HIEU_LUC', FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG), FOREIGN KEY (MA_KH) REFERENCES NGUOI_THUE(MA_KH) );
CREATE TABLE HOA_DON ( MA_HD_BILL NUMBER PRIMARY KEY, MA_HD NUMBER, THANG_NAM VARCHAR2(7), TONG_TIEN NUMBER, TRANG_THAI VARCHAR2(20) DEFAULT 'CHUA_TRA', D_CU NUMBER, D_MOI NUMBER, N_CU NUMBER, N_MOI NUMBER, FOREIGN KEY (MA_HD) REFERENCES HOP_DONG(MA_HD) );
CREATE TABLE TAI_SAN ( MA_TS NUMBER PRIMARY KEY, TEN_TS VARCHAR2(100), TINH_TRANG VARCHAR2(100), GIA_TRI NUMBER, TI_LE_MOI NUMBER, MA_PHONG NUMBER, FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG) );
CREATE TABLE THANH_VIEN ( MA_TV NUMBER PRIMARY KEY, MA_PHONG NUMBER, MA_KH NUMBER, FOREIGN KEY (MA_PHONG) REFERENCES PHONG(MA_PHONG), FOREIGN KEY (MA_KH) REFERENCES NGUOI_THUE(MA_KH) );
CREATE TABLE CHI_PHI ( MA_CHI_PHI NUMBER PRIMARY KEY, LOAI_CHI_PHI NVARCHAR2(100), SO_TIEN NUMBER, NGAY_CHI DATE DEFAULT SYSDATE, GHI_CHU NVARCHAR2(255) );
CREATE TABLE DICH_VU ( MA_DV NUMBER PRIMARY KEY, TEN_DV VARCHAR2(50), DON_GIA NUMBER );

