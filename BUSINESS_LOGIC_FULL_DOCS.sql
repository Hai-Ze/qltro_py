  SELECT "MA_HD","MA_KH","THANG_NAM","TONG_TIEN","NGAY_LAP","TRANG_THAI" FROM (
    SELECT * FROM HOA_DON ORDER BY TONG_TIEN DESC
)
WHERE ROWNUM <= 5;

-- FUNCTION: F_CON_CHO
-- LỆNH TEST: SELECT F_CON_CHO() FROM DUAL;
CREATE OR REPLACE FUNCTION F_CON_CHO(p_MA_HD NUMBER)
RETURN NUMBER
IS
    v_count NUMBER;
    v_max NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM CHI_TIET_THUE
    WHERE MA_HD = p_MA_HD;
    SELECT P.SO_NGUOI_TOI_DA INTO v_max
    FROM PHONG P JOIN HOP_DONG H
    ON P.MA_PHONG = H.MA_PHONG
    WHERE H.MA_HD = p_MA_HD;
    IF v_count < v_max THEN
        RETURN 1;
    ELSE
        RETURN 0;
    END IF;
END;
/

