-- LỆNH TEST: SELECT F_TONG_TIEN(1001) FROM DUAL;
CREATE OR REPLACE FUNCTION F_TONG_TIEN(p_MA_HD NUMBER)
RETURN NUMBER
IS
    v NUMBER;
BEGIN
    SELECT NVL(SUM(SO_TIEN),0)
    INTO v
    FROM THANH_TOAN
    WHERE MA_HD = p_MA_HD;
