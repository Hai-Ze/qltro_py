    PROCEDURE P_DS_HOPDONG(ds OUT SYS_REFCURSOR);

    -- TOP hợp đồng (có ROWNUM)
    PROCEDURE P_TOP_HOPDONG(ds OUT SYS_REFCURSOR, p_TOP NUMBER);

END PKG_PHONG_TRO;
