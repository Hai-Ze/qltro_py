-- ========================
-- PROCEDURE: TOP (ROWNUM)
-- ========================
PROCEDURE P_TOP_HOPDONG(ds OUT SYS_REFCURSOR, p_TOP NUMBER)
AS
BEGIN
    OPEN ds FOR
    SELECT * FROM (
        SELECT H.MA_HD, H.MA_PHONG, SUM(T.SO_TIEN) TONG_TIEN
        FROM HOP_DONG H
        JOIN THANH_TOAN T ON H.MA_HD = T.MA_HD
        GROUP BY H.MA_HD, H.MA_PHONG
        ORDER BY TONG_TIEN DESC
    )
    WHERE ROWNUM <= p_TOP;
END;

