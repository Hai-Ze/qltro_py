CREATE OR REPLACE VIEW V_DS_PHONG AS
SELECT P.MA_PHONG, P.TEN_PHONG, P.GIA,
       P.SO_NGUOI_TOI_DA,
       COUNT(CT.MA_NGUOI) AS SO_NGUOI,
       P.TRANG_THAI
FROM PHONG P
LEFT JOIN HOP_DONG H 
  ON P.MA_PHONG = H.MA_PHONG 
 AND H.TRANG_THAI = 'CON_HIEU_LUC'
LEFT JOIN CHI_TIET_THUE CT 
  ON H.MA_HD = CT.MA_HD
GROUP BY P.MA_PHONG, P.TEN_PHONG, P.GIA, P.SO_NGUOI_TOI_DA, P.TRANG_THAI;

CREATE OR REPLACE VIEW V_HOPDONG_TIEN AS
SELECT H.MA_HD, H.MA_PHONG,
       NVL(SUM(T.SO_TIEN),0) AS TONG_TIEN
FROM HOP_DONG H
LEFT JOIN THANH_TOAN T ON H.MA_HD = T.MA_HD
GROUP BY H.MA_HD, H.MA_PHONG;

CREATE OR REPLACE FUNCTION F_TONG_TIEN(p_MA_HD NUMBER)
RETURN NUMBER
IS
    v NUMBER;
BEGIN
    SELECT NVL(SUM(SO_TIEN),0)
    INTO v
    FROM THANH_TOAN
    WHERE MA_HD = p_MA_HD;

    RETURN v;
END;
/

CREATE OR REPLACE FUNCTION F_CON_CHO(p_MA_HD NUMBER)
RETURN NUMBER
IS
    v_count NUMBER;
    v_max NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM CHI_TIET_THUE
    WHERE MA_HD = p_MA_HD;

    SELECT P.SO_NGUOI_TOI_DA INTO v_max
    FROM PHONG P JOIN HOP_DONG H 
    ON P.MA_PHONG = H.MA_PHONG
    WHERE H.MA_HD = p_MA_HD;

    IF v_count < v_max THEN
        RETURN 1;
    ELSE
        RETURN 0;
    END IF;
END;
/


