CREATE OR REPLACE TRIGGER TRG_CHECK_SONGUOI
BEFORE INSERT ON CHI_TIET_THUE
FOR EACH ROW
DECLARE
    v_count NUMBER;
    v_max NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM CHI_TIET_THUE
    WHERE MA_HD = :NEW.MA_HD;

    SELECT P.SO_NGUOI_TOI_DA INTO v_max
    FROM PHONG P JOIN HOP_DONG H 
    ON P.MA_PHONG = H.MA_PHONG
    WHERE H.MA_HD = :NEW.MA_HD;

    IF v_count >= v_max THEN
        RAISE_APPLICATION_ERROR(-20001, 'Vuot so nguoi!');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_UPDATE_PHONG
AFTER INSERT ON HOP_DONG
FOR EACH ROW
BEGIN
    UPDATE PHONG
    SET TRANG_THAI = 'DA_THUE'
    WHERE MA_PHONG = :NEW.MA_PHONG;
END;
