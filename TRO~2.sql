    PROCEDURE P_THEM_NGUOI(p_MA_HD NUMBER, p_MA_NGUOI NUMBER);

    -- Tạo hợp đồng
    PROCEDURE P_TAO_HOPDONG(p_MA_PHONG NUMBER, p_TIEN_COC NUMBER);

